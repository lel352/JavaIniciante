package training.cursojava.exercicios.aulas34;

public class ConversaoDeUnidadesDeVolumes {
	
	public static double litrosCentimetrosParaCubicos(double valor) {
		return valor*1000;
	}
	
	public static double metroCubicoParaLitros(double valor) {
		return valor*1000;
	}
	
	public static double metroCubicoParaPesLitros(double valor) {
		return valor*35.32;
	}
	
	public static double galaoAmericanoParaPolegadasCubicas(double valor) {
		return valor*231;
	}
	
	public static double galaoAmericanoParaLitros(double valor) {
		return valor*3.785;
	}
}
