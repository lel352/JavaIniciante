package training.cursojava.exercicios.aulas34;

public class ConversaoDeUnidadesDeArea {

	public static double metroQuadradoParaPesQuadrado(double valor) {
		return valor*10.76;
	}
	
	public static double peQuadradoParaCentimetro(double valor) {
		return valor*929;
	}
	
	public static double milhaQuadradoParaAcres(double valor) {
		return valor*640;
	}
	
	public static double acreParaPesQuadrados(double valor) {
		return valor*43.560;
	}
	
}
