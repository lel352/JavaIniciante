package training.cursojava.exercicios.aulas34;

public class ConversaoDeUnidadesDeTempo {
	
	public static int mintutosParaSegundos(int valor) {
		return valor*60;
	}
	
	public static int horaParaMinutos(int valor) {
		return valor*60;
	}
	
	public static int diaParaHoras(int valor) {
		return valor*24;
	}
	
	public static int semanaParaDias(int valor) {
		return valor*7;
	}
	
	public static int mesParaDias(int valor) {
		return valor*30;
	}
	
	public static double anoParaDias(int valor) {
		return valor*365.25;
	}
}
