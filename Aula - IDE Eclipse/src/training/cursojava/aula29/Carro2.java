package training.cursojava.aula29;

public class Carro2 {
	String marca;
	String modelo;
	int numeroPassageiros;
	double capacidaCombustivel;
	double consumoCombustivel;
	
	
	public Carro2() {
		super();
	}

	public Carro2(String marca, String modelo, int numeroPassageiros, double capacidaCombustivel,
			double consumoCombustivel) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.numeroPassageiros = numeroPassageiros;
		this.capacidaCombustivel = capacidaCombustivel;
		this.consumoCombustivel = consumoCombustivel;
	}
}
