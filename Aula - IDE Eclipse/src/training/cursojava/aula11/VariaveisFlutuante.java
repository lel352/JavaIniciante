package training.cursojava.aula11;

public class VariaveisFlutuante {

	public static void main(String[] args) {
		
		double valorPassagem = 3.50;
		float valorMeiaPassagem = 1.75f;
		
		System.out.println("Valor passagem "+valorPassagem);
		System.out.println("Valor meia passagem "+valorMeiaPassagem);
		System.out.printf("Valor passagem %.2f",valorPassagem);
		 
		double notacao = 1.234e2; //igual a 123.4
		System.out.println("\n notaçao "+notacao);
  
	}

}
